        activity()
            ->withProperties(['string' => $query])
            ->log('Wyszukanie w UNMS');

        $result = app('App\UNMS')->search($query);

        if(!$result) {
            return response()->json(null, 400);
        }

        return response()->json($result, 200);
    }

