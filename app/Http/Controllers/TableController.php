<?php

namespace App\Http\Controllers;

use App\Table;
use Illuminate\Http\Request;

class TableController extends Controller
{
    public function __construct() {
        $this->middleware('permission:table-list');
        $this->middleware('permission:table-store', ['only' => ['store',]]);
        $this->middleware('permission:table-get', ['only' => ['create', 'show', 'index']]);
        $this->middleware('permission:table-edit', ['only' => ['edit', 'update']]);
        $this->middleware('permission:table-delete', ['only' => ['delete']]);
    }
    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
