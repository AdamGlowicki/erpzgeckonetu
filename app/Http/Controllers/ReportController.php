     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\JsonResponse
     */
    public function index($month, $user = null)
    {
        $result = Report::where('date', 'LIKE', $month . '%')
            ->where('user_id', $user ?? auth()->user()->id)
            ->get();

        return response()->json([
            'data' => $result
        ], 200);
